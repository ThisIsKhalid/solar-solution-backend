const mongoose = require("mongoose");

const newsEventSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const NewsEvent = mongoose.model("NewsEvent", newsEventSchema);

module.exports = NewsEvent;
